# 1. Single responsibility Principle
#    (zasada pojedynczej odpowiedzialnosci - kazda klasa/modul/funkcja/komponent
#     powinny miec jedna i tylko jedna dobrze zdefiniowana odpowiedzialnosc
# 2. Open/Cloes Principle
#    (zasada otwarty/zamkniety) - powinnismy tak konstruowac klasy by byly one otwarte
#    na rozszerzenie ale zamkniete na modyfikacje.
# 3. Liskov Substitution Principle
#    (zasada podstawienia Barbary Liskov) - klasa potomna powinna byc szczegolnym
#    przypadkiem klasy bazowej
# 4. Interface Segregation Principle
#    (zasada segragacji interfejsow) - wiele bardziej wyspecjalizowanych
#    interfejsow jest lepsze niz jeden ogolny.
# 5. Dependency Inversion Principle
#    (zasada odwrócenia zaleznosci) - nasze klasy powinny opierać się na abstrakcji,
#    a nie szczegółach implementacji.


